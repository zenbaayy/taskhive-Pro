    <script>
        function previewImage(input) {
            if (input.files && input.files[0]) {
                var reader = new FileReader();
                reader.onload = function(e) {
                    let preview = document.getElementById('avatar-preview');
                    let placeholder = document.getElementById('avatar-placeholder');
                    
                    if (!preview) {
                        preview = document.createElement('img');
                        preview.id = 'avatar-preview';
                        preview.className = 'w-20 h-20 rounded-full object-cover border-2 border-indigo-500 shadow-sm';
                        placeholder.parentNode.replaceChild(preview, placeholder);
                    }
                    preview.src = e.target.result;
                }
                reader.readAsDataURL(input.files[0]);
            }
        }
    </script>
